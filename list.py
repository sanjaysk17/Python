food=["Apple","Strawberry","Pizza","Hamburger","Icecream"]
print(food)
food.append("Coffee")##To append the item in last
print(food)
food.pop()##To remove the last element
print(food)
food.insert(2,"Tea")##to insert at Desired Index
print(food)
food.remove("Apple")##To remove Specific item
print(food)
food.sort()##To sort
print(food)
food.clear()##To clear all the list
print(food)